#include "libft.h"

int	ft_atoi(const char *str)
{
	char	*s;
	size_t	sign;
	size_t	n;

	s = (char *)str;
	sign = 1;
	n = 0;
	while (*s == ' ' || *s == '\t' || *s == '\n' || *s == '\r' || *s == '\v'
		|| *s == '\f')
		s++;
	if (*s == '-' || *s == '+')
		sign = 1 - 2 * (*s++ == '-');
	while (*s >= '0' && *s <= '9')
		n = n * 10 + *s++ - '0';
	return (n * sign);
}
